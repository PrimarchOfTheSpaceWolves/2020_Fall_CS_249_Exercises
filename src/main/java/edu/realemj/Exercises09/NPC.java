package edu.realemj.Exercises09;

public class NPC {
    private String name;
    private int strength;

    public NPC(String n) {
        name = n;
    }

    public String getName() {
        return name;
    }

    public void setStrength(int s) {
        strength = s;
    }

    public int getStrength() {
        return strength;
    }
}
